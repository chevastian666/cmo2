# Process each file
for file in "${files[@]}"; do
  echo "Processing $file..."
  
  # Create a temporary file
  temp_file="${file}.tmp"
  cp "$file" "$temp_file"
  
  # Apply each replacement
  for replacement in "${replacements[@]}"; do
    # Split the replacement into from:to
    from=$(echo "$replacement" | cut -d: -f1)
    to=$(echo "$replacement" | cut -d: -f2)
    
    # Use sed to replace whole words only
    sed -i '' "s/\b${from}\b/${to}/g" "$temp_file"
  done
  
  # Move the temporary file back
  mv "$temp_file" "$file"
done

echo "Underscore prefix removal complete!"